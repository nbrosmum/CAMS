    private void EditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditButtonActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)ATable.getModel();
        SimpleDateFormat DateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String time = "";
            if(T1RadioButton.isSelected()){
                time = "10.00 a.m.-12.00 p.m.";
            }else if(T2RadioButton.isSelected()){
                time = "1.00 p.m.-3.00 p.m.";
            }else{
                time = "4.00 p.m.-6.00 p.m.";
            }
        if(ATable.getSelectedRowCount() == 1){
            String Name = NTextField.getText();
            String Contact = HPTextField.getText();
            String Date = DateFormat.format(DateChooser.getDate());
            String Time = time;
            
            tblModel.setValueAt(Name, ATable.getSelectedRow(), 0);
            tblModel.setValueAt(Contact, ATable.getSelectedRow(), 1);
            tblModel.setValueAt(Date, ATable.getSelectedRow(), 2);
            tblModel.setValueAt(Time, ATable.getSelectedRow(), 3);
            
            JOptionPane.showMessageDialog(null, "Edit Successful !");
            
            DBFile db = new DBFile();
            db.ExportTable("Appointment.txt", ATable);

        }else{
            if(ATable.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "Table is Emtpy !");
            }else{
                JOptionPane.showMessageDialog(null, "Please Select a Single Row for Edit !");
            }
        }
        
    }//GEN-LAST:event_EditButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)ATable.getModel();
        if(ATable.getSelectedRowCount()==1){
            tblModel.removeRow(ATable.getSelectedRow());
            JOptionPane.showMessageDialog(null, "The Record had been Delete !");
            DBFile db = new DBFile();
            db.ExportTable("Appointment.txt", ATable);
            
        }else{
            if(ATable.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "The Table is Emtpy !");
            }else{
                JOptionPane.showMessageDialog(null, "Please select a record to delete !");
            }
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

