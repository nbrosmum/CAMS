    private void ATableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ATableMouseClicked
        try{
        DefaultTableModel tblModel = (DefaultTableModel)ATable.getModel();
        String tblName = tblModel.getValueAt(ATable.getSelectedRow(), 0 ).toString();
        String tblContact = tblModel.getValueAt(ATable.getSelectedRow(), 1 ).toString();
        Date tblDate = new SimpleDateFormat("dd-MM-yyyy").parse((String)tblModel.getValueAt(ATable.getSelectedRow(), 2).toString());
        String tblTime = tblModel.getValueAt(ATable.getSelectedRow(), 3 ).toString();
        NTextField.setText(tblName);
        HPTextField.setText(tblContact);
        DateChooser.setDate(tblDate);
        if(tblTime.equals("10.00 a.m.-12.00 p.m.")){
            T1RadioButton.setSelected(rootPaneCheckingEnabled);
        }else if(tblTime.equals("1.00 p.m.-3.00 p.m.")){
            T2RadioButton.setSelected(rootPaneCheckingEnabled);
        }else{
            T3RadioButton.setSelected(rootPaneCheckingEnabled);
        }
        
        }catch(ParseException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_ATableMouseClicked

    private void StatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StatusButtonActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)ATable.getModel();
        String Status = "";
        if(ATable.getSelectedRowCount() == 1){
            String[] Select = {"Approve", "Reject","Cancel"};
                var role = JOptionPane.showOptionDialog(null, "Confrim for the Appointment !", "Confrim Appointment", 0, 3, null,Select, Select[0]);
                if(role == 0){
                    Status = "Apporve";
                }else if(role == 1){
                    Status = "Reject";     
                }else{
                    Status = "Cancel"; 
                }
            tblModel.setValueAt(Status, ATable.getSelectedRow(), 4);
            DBFile db = new DBFile();
            db.ExportTable("Appointment.txt", ATable);
        }else{
            if(ATable.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "The Table is Emtpy !");
            }else{
                JOptionPane.showMessageDialog(null, "Please select a record to Check Status !");
            }
        }
        
    }//GEN-LAST:event_StatusButtonActionPerformed

